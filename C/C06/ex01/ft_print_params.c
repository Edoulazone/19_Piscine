#include <unistd.h>

int	main(int ac, char **av)
{
	int	j;
	int	i;

	j = 1;
	while (j < ac)
	{
		i = 0;
		while (av[j][i])
		{
			write(1, &av[j][i], 1);
			i++;
		}
		j++;
		write(1, "\n", 1);
	}
	return (0);
}
