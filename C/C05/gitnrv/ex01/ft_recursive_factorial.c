int	ft_recursive_factorial(int nb)
{
	int	i;

	i = nb;
	if (nb < 0)
		return (0);
	if (nb == 0)
		return (1);
	if (nb > 1)
	{
		i = i * ft_recursive_factorial(nb - 1);
	}
	return (i);
}
